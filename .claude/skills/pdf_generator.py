# -*- coding: utf-8 -*-
"""
PDF 報告生成器 - 直接生成 PDF，無需 PowerPoint
使用 reportlab 庫
"""

from reportlab.lib import colors
from reportlab.lib.pagesizes import A4
from reportlab.lib.styles import getSampleStyleSheet, ParagraphStyle
from reportlab.lib.units import cm, inch
from reportlab.platypus import SimpleDocTemplate, Table, TableStyle, Paragraph, Spacer, PageBreak, Image
from reportlab.lib.enums import TA_CENTER, TA_LEFT, TA_RIGHT
from reportlab.pdfgen import canvas
import json
import os
from datetime import datetime


class PDFReportGenerator:
    """PDF 報告生成器"""

    def __init__(self, output_path):
        self.output_path = output_path
        self.styles = getSampleStyleSheet()

        # 自定義樣式
        self.styles.add(ParagraphStyle(
            name='Title_CN',
            parent=self.styles['Title'],
            fontName='Helvetica',
            fontSize=24,
            textColor=colors.HexColor('#003366'),
            spaceAfter=20
        ))

        self.styles.add(ParagraphStyle(
            name='Heading_CN',
            parent=self.styles['Heading1'],
            fontName='Helvetica',
            fontSize=16,
            textColor=colors.HexColor('#003366'),
            spaceBefore=15,
            spaceAfter=10
        ))

        self.styles.add(ParagraphStyle(
            name='SubHeading',
            parent=self.styles['Heading2'],
            fontName='Helvetica',
            fontSize=12,
            textColor=colors.HexColor('#006699'),
            spaceBefore=10,
            spaceAfter=5
        ))

        self.styles.add(ParagraphStyle(
            name='Body_CN',
            parent=self.styles['BodyText'],
            fontName='Helvetica',
            fontSize=10,
            leading=14
        ))

    def create_header_footer(self, canvas, doc):
        """頁眉頁腳"""
        canvas.saveState()

        # 頁眉
        canvas.setFont('Helvetica', 9)
        canvas.setFillColor(colors.HexColor('#666666'))
        canvas.drawString(2*cm, A4[1] - 1.5*cm, "Global Energy Storage Market Report 2025-2030")
        canvas.drawRightString(A4[0] - 2*cm, A4[1] - 1.5*cm, datetime.now().strftime('%Y-%m-%d'))

        # 頁腳
        canvas.drawString(2*cm, 1.5*cm, "Generated by Claude Code")
        canvas.drawRightString(A4[0] - 2*cm, 1.5*cm, f"Page {doc.page}")

        canvas.restoreState()

    def add_cover_page(self, story, title, subtitle):
        """添加封面"""
        story.append(Spacer(1, 5*cm))

        # 標題
        title_style = ParagraphStyle(
            'CoverTitle',
            fontName='Helvetica-Bold',
            fontSize=28,
            textColor=colors.HexColor('#003366'),
            alignment=TA_CENTER,
            spaceAfter=30
        )
        story.append(Paragraph(title, title_style))

        story.append(Spacer(1, 2*cm))

        # 副標題
        sub_style = ParagraphStyle(
            'CoverSub',
            fontName='Helvetica',
            fontSize=16,
            textColor=colors.HexColor('#666666'),
            alignment=TA_CENTER,
            spaceAfter=20
        )
        story.append(Paragraph(subtitle, sub_style))

        # 日期
        date_style = ParagraphStyle(
            'CoverDate',
            fontName='Helvetica',
            fontSize=12,
            textColor=colors.HexColor('#999999'),
            alignment=TA_CENTER
        )
        story.append(Spacer(1, 3*cm))
        story.append(Paragraph(datetime.now().strftime('%Y-%m-%d'), date_style))

        story.append(PageBreak())

    def add_executive_summary(self, story, data):
        """添加執行摘要"""
        story.append(Paragraph("Executive Summary", self.styles['Heading_CN']))

        segments = data.get('segments', [])
        total_market = data.get('total_market_5yr', 0)
        fastest = data.get('fastest_growing', [])[:3]

        # 核心數據
        summary_data = [
            ['Metric', 'Value'],
            ['Total Market 2025-2030', f'{total_market:,.0f} 億元'],
            ['Market Segments', str(len(segments))],
            ['Fastest Growing', f'{fastest[0]["name"] if fastest else "N/A"} ({fastest[0]["cagr_2025_2030"] if fastest else 0}%)'],
            ['Largest Market', f'{segments[0]["name"] if segments else "N/A"} ({segments[0]["share_2025_2030"] if segments else 0}%)']
        ]

        table = Table(summary_data, colWidths=[8*cm, 8*cm])
        table.setStyle(TableStyle([
            ('BACKGROUND', (0, 0), (-1, 0), colors.HexColor('#003366')),
            ('TEXTCOLOR', (0, 0), (-1, 0), colors.white),
            ('ALIGN', (0, 0), (-1, -1), 'CENTER'),
            ('FONTNAME', (0, 0), (-1, 0), 'Helvetica-Bold'),
            ('FONTSIZE', (0, 0), (-1, -1), 10),
            ('BOTTOMPADDING', (0, 0), (-1, 0), 12),
            ('BACKGROUND', (0, 1), (-1, -1), colors.HexColor('#F5F7FA')),
            ('GRID', (0, 0), (-1, -1), 1, colors.HexColor('#CCCCCC')),
        ]))
        story.append(table)
        story.append(Spacer(1, 1*cm))

    def add_market_table(self, story, data):
        """添加市場數據表格"""
        story.append(Paragraph("Market Overview", self.styles['Heading_CN']))

        segments = data.get('segments', [])

        # 表格數據
        table_data = [['Rank', 'Segment', '5Y Total', 'CAGR', 'Share', '2030 Scale']]
        for i, seg in enumerate(segments[:10], 1):
            table_data.append([
                str(i),
                seg['name'],
                f"{seg['cumulative_5yr']:,.0f}",
                f"{seg['cagr_2025_2030']:.1f}%",
                f"{seg['share_2025_2030']:.2f}%",
                f"{seg['scale_2030']:,.0f}"
            ])

        table = Table(table_data, colWidths=[2*cm, 4*cm, 3*cm, 2.5*cm, 2.5*cm, 3*cm])
        table.setStyle(TableStyle([
            ('BACKGROUND', (0, 0), (-1, 0), colors.HexColor('#003366')),
            ('TEXTCOLOR', (0, 0), (-1, 0), colors.white),
            ('ALIGN', (0, 0), (0, -1), 'CENTER'),
            ('ALIGN', (2, 1), (-1, -1), 'RIGHT'),
            ('FONTNAME', (0, 0), (-1, 0), 'Helvetica-Bold'),
            ('FONTSIZE', (0, 0), (-1, -1), 9),
            ('BOTTOMPADDING', (0, 0), (-1, 0), 10),
            ('TOPPADDING', (0, 0), (-1, -1), 6),
            ('BACKGROUND', (0, 1), (-1, -1), colors.white),
            ('ROWBACKGROUNDS', (0, 1), (-1, -1), [colors.white, colors.HexColor('#F5F7FA')]),
            ('GRID', (0, 0), (-1, -1), 0.5, colors.HexColor('#CCCCCC')),
            ('FONTNAME', (0, 1), (-1, 3), 'Helvetica-Bold'),
        ]))
        story.append(table)
        story.append(Spacer(1, 1*cm))

    def add_growth_ranking(self, story, data):
        """添加增速排名"""
        story.append(Paragraph("Growth Ranking (CAGR)", self.styles['Heading_CN']))

        fastest = data.get('fastest_growing', [])[:8]

        # 排名數據
        rank_data = [['Rank', 'Segment', 'CAGR', '5Y Total']]
        for i, seg in enumerate(fastest, 1):
            rank_data.append([
                str(i),
                seg['name'],
                f"{seg['cagr_2025_2030']:.1f}%",
                f"{seg['cumulative_5yr']:,.0f}"
            ])

        table = Table(rank_data, colWidths=[2*cm, 5*cm, 3*cm, 4*cm])

        # 根據 CAGR 設置顏色
        style = [
            ('BACKGROUND', (0, 0), (-1, 0), colors.HexColor('#006699')),
            ('TEXTCOLOR', (0, 0), (-1, 0), colors.white),
            ('ALIGN', (0, 0), (-1, -1), 'CENTER'),
            ('FONTNAME', (0, 0), (-1, 0), 'Helvetica-Bold'),
            ('FONTSIZE', (0, 0), (-1, -1), 10),
            ('BOTTOMPADDING', (0, 0), (-1, 0), 10),
            ('GRID', (0, 0), (-1, -1), 0.5, colors.HexColor('#CCCCCC')),
        ]

        # 前三名加粗
        for i in range(1, 4):
            style.append(('FONTNAME', (0, i), (-1, i), 'Helvetica-Bold'))

        table.setStyle(TableStyle(style))
        story.append(table)
        story.append(Spacer(1, 1*cm))

    def add_conclusion(self, story, data):
        """添加結論"""
        story.append(Paragraph("Conclusion", self.styles['Heading_CN']))

        segments = data.get('segments', [])
        fastest = data.get('fastest_growing', [])[:3]

        conclusions = f"""
<b>1. Market Scale</b><br/>
The total market size for 2025-2030 reaches {data.get('total_market_5yr', 0):,.0f} billion RMB,
with EV dominating at {segments[0]['share_2025_2030'] if segments else 0}% market share.

<br/><br/>
<b>2. Growth Trends</b><br/>
Emerging segments show explosive growth:
- AI Storage: {fastest[0]['cagr_2025_2030'] if len(fastest) > 0 else 0}% CAGR
- Robot Storage: {fastest[1]['cagr_2025_2030'] if len(fastest) > 1 else 0}% CAGR
- Drone Storage: {fastest[2]['cagr_2025_2030'] if len(fastest) > 2 else 0}% CAGR

<br/><br/>
<b>3. Future Outlook</b><br/>
The market is accelerating towards electrification and intelligence,
with continuous emergence of new technologies and applications.
        """

        story.append(Paragraph(conclusions, self.styles['Body_CN']))

    def generate(self, title, subtitle, data):
        """生成 PDF"""
        doc = SimpleDocTemplate(
            self.output_path,
            pagesize=A4,
            rightMargin=2*cm,
            leftMargin=2*cm,
            topMargin=2*cm,
            bottomMargin=2*cm
        )

        story = []

        # 封面
        self.add_cover_page(story, title, subtitle)

        # 執行摘要
        self.add_executive_summary(story, data)

        # 市場數據
        self.add_market_table(story, data)
        story.append(PageBreak())

        # 增速排名
        self.add_growth_ranking(story, data)

        # 結論
        self.add_conclusion(story, data)

        # 生成
        doc.build(story, onFirstPage=self.create_header_footer, onLaterPages=self.create_header_footer)
        print(f"[OK] PDF generated: {self.output_path}")
        return self.output_path


def pptx_to_pdf(pptx_path, output_dir=None):
    """將市場分析 PPTX 轉換為 PDF"""
    if output_dir is None:
        output_dir = os.path.dirname(pptx_path)

    base_name = os.path.splitext(os.path.basename(pptx_path))[0]
    json_path = pptx_path.replace('.pptx', '_market.json')

    # 如果沒有對應的 JSON，嘗試其他位置
    if not os.path.exists(json_path):
        json_path = os.path.join(output_dir, f"{base_name}_market.json")

    if not os.path.exists(json_path):
        print(f"[ERROR] Market data not found: {json_path}")
        return None

    pdf_path = os.path.join(output_dir, f"{base_name}_report.pdf")

    with open(json_path, 'r', encoding='utf-8') as f:
        data = json.load(f)

    generator = PDFReportGenerator(pdf_path)
    generator.generate(
        title="Global Energy Storage Market\nAnalysis Report 2025-2030",
        subtitle="11 Market Segments | Market Analysis",
        data=data
    )

    return pdf_path


if __name__ == "__main__":
    import sys

    if len(sys.argv) < 2:
        print("Usage:")
        print("  python pdf_generator.py <market_json> [output_pdf]")
        print("  python pdf_generator.py <pptx_file> (auto-find json)")
        sys.exit(1)

    input_path = sys.argv[1]
    output_path = sys.argv[2] if len(sys.argv) > 2 else None

    if input_path.endswith('.pptx'):
        # 從 PPTX 生成
        if output_path:
            pptx_to_pdf(input_path, os.path.dirname(output_path))
        else:
            pptx_to_pdf(input_path)
    else:
        # 從 JSON 生成
        with open(input_path, 'r', encoding='utf-8') as f:
            data = json.load(f)

        if output_path is None:
            output_path = input_path.replace('.json', '_report.pdf')

        generator = PDFReportGenerator(output_path)
        generator.generate(
            title="Market Analysis Report",
            subtitle="Data Analysis",
            data=data
        )
